# ler duas notas e mostrar a média delas.
n1 = float(input('Digite a primeira nota:'))
n2 = float(input('Digite a segunda nota:'))
print('A primeira nota é {}, a segunda nota é {}, sendo assim a média é {:.1f}'
      .format(n1, n2, (n1+n2)/2))
