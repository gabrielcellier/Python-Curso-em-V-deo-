# ler um número e mostrar seu dobro, triplo e raiz quadrada
print('Digite um valor, iremos calcular seu dobro, triplo e sua raíz quadrada.')
num = float(input('Digite um valor numérico: '))
print(f'O valor digitado foi {num}. Seu dobro é {num*2}. Seu triplo {num*3}.'
      f'Sua raíz quadrada {num**2}.')
